window.SITE = SITE;

const selectOne = (selector, scope = document) => scope.querySelector(selector);
const selectAll = (selector, scope = document) => [...scope.querySelectorAll(selector)];

function mapUrl() {
  return SITE.mapLink;
}

function mapEmbedUrl() {
  return SITE.mapEmbed;
}

function sharedActions(page) {
  if (page === "booking") {
    return {
      header: {
        href: SITE.whatsapp,
        label: "WhatsApp",
        external: true
      },
      footerPrimary: {
        href: SITE.whatsapp,
        label: "WhatsApp",
        external: true
      },
      footerSecondary: {
        href: "/services/",
        label: "Ver servicios",
        external: false
      }
    };
  }

  return {
    header: {
      href: "/booking/",
      label: "Reservar cita",
      external: false
    },
    footerPrimary: {
      href: "/booking/",
      label: "Reservar ahora",
      external: false
    },
    footerSecondary: {
      href: SITE.whatsapp,
      label: "WhatsApp",
      external: true
    }
  };
}

function createNavLink(item, page) {
  const isActive = item.page === page;
  const ariaCurrent = isActive ? ' aria-current="page"' : "";
  return `<a class="nav-link${isActive ? " is-active" : ""}" href="${item.href}"${ariaCurrent}>${item.label}</a>`;
}

function closeNav(toggle, nav) {
  if (!toggle || !nav) return;
  toggle.setAttribute("aria-expanded", "false");
  toggle.setAttribute("aria-label", "Abrir menú");
  nav.classList.remove("is-open");
  document.body.classList.remove("nav-open");
}

function openNav(toggle, nav) {
  if (!toggle || !nav) return;
  toggle.setAttribute("aria-expanded", "true");
  toggle.setAttribute("aria-label", "Cerrar menú");
  nav.classList.add("is-open");
  document.body.classList.add("nav-open");
}

function renderHeader() {
  const mount = selectOne("[data-site-header]");
  if (!mount) return;

  const page = document.body.dataset.page;
  const navLinks = SITE.navPages.map((item) => createNavLink(item, page)).join("");
  const actions = sharedActions(page);
  const headerTarget = actions.header.external
    ? `target="_blank" rel="noreferrer"`
    : "";

  mount.innerHTML = `
    <a class="skip-link" href="#main-content">Saltar al contenido</a>
    <header class="site-header">
      <div class="container nav-shell">
        <a class="brand" href="/" aria-label="Ir al inicio de ${SITE.name}">
          <img class="brand-logo" src="${SITE.logoPrimary}" alt="Logo de ${SITE.name}" width="72" height="72" />
          <span class="brand-copy">
            <strong>${SITE.name}</strong>
            <small>${SITE.tagline}</small>
          </span>
        </a>
        <button
          class="nav-toggle"
          type="button"
          aria-label="Abrir menú"
          aria-expanded="false"
          aria-controls="site-nav"
        >
          <span></span>
          <span></span>
          <span></span>
        </button>
        <nav class="site-nav" id="site-nav" aria-label="Navegación principal">
          ${navLinks}
          <a class="button button-small nav-cta" href="${actions.header.href}" ${headerTarget} aria-label="${actions.header.label} en ${SITE.name}">${actions.header.label}</a>
        </nav>
      </div>
    </header>
  `;

  const toggle = selectOne(".nav-toggle", mount);
  const nav = selectOne(".site-nav", mount);

  toggle?.addEventListener("click", () => {
    const expanded = toggle.getAttribute("aria-expanded") === "true";
    if (expanded) {
      closeNav(toggle, nav);
    } else {
      openNav(toggle, nav);
    }
  });

  nav?.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => closeNav(toggle, nav));
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") closeNav(toggle, nav);
  });
}

